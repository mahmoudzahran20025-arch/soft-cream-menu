let iconsLoaded = false;
const interactionEvents = ['click', 'scroll', 'touchstart', 'mousemove'];

/**
 * يقوم بإدراج الأيقونات الثانوية في نهاية الـ body.
 */
export function loadSecondaryIcons() {
  if (iconsLoaded || document.getElementById('secondary-icons-sprite')) {
    return; // تم التحميل مسبقاً
  }
  
  iconsLoaded = true;
  document.body.insertAdjacentHTML('beforeend', secondaryIconsHTML);
  
  // تنظيف الـ event listeners
  interactionEvents.forEach(event => {
    document.removeEventListener(event, lazyLoadIcons);
  });
}

/**
 * دالة التحميل الكسول التي يتم استدعاؤها عند التفاعل.
 */
function lazyLoadIcons() {
  loadSecondaryIcons();
}

/**
 * الاستماع لأول تفاعل من المستخدم لتحميل الأيقونات.
 * نستخدم { once: true, passive: true } لأفضل أداء.
 */
interactionEvents.forEach(event => {
  document.addEventListener(event, lazyLoadIcons, { once: true, passive: true });
});

/**
 * كـ Fallback، نقوم بتحميل الأيقونات بعد فترة قصيرة
 * إذا كان المتصفح في وضع الخمول (idle).
 */
if ('requestIdleCallback' in window) {
  requestIdleCallback(loadSecondaryIcons, { timeout: 3000 });
} else {
  // fallback لمتصفحات أقدم
  setTimeout(loadSecondaryIcons, 3000);
}